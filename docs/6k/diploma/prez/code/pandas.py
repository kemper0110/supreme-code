import pandas as pd

# Представьте, что это данные из Excel или CSV
data = {
    'Товар': ['Ноутбук', 'Мышь', 'Клавиатура', 'Монитор', 'Колонки'],
    'Категория': ['Электроника', 'Аксессуары', 'Аксессуары', 'Электроника', 'Аксессуары'],
    'Цена': [50000, 1500, 3000, 25000, 4000],
    'Количество': [3, 15, 8, 2, 7]
}

df = pd.DataFrame(data)
print("Исходная таблица:")
print(df)

print("\n💰 Выручка по категориям:")
print(df.groupby('Категория').apply(lambda x: (x['Цена'] * x['Количество']).sum()))

print("\n🏆 Топ-2 товара по выручке:")
df['Выручка'] = df['Цена'] * df['Количество']
print(df.nlargest(2, 'Выручка')[['Товар', 'Выручка']])
